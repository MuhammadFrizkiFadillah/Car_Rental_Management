    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        String kode =
        txtKodePenyewa.getText();

        String nama =
        txtNama.getText().trim();

        String alamat =
        txtAlamat.getText().trim();

        String noHp =
        txtNoHp.getText().trim();

        if (nama.isEmpty()
            || alamat.isEmpty()
            || noHp.isEmpty()) {

            JOptionPane.showMessageDialog(
                this,
                "Semua data wajib diisi!");

            return;
        }

        Penyewa penyewa =
        new Penyewa();

        penyewa.setKodePenyewa(
            kode);

        penyewa.setNama(
            nama);

        penyewa.setAlamat(
            alamat);

        penyewa.setNoHp(
            noHp);

        penyewa.setDeleted(
            false);

        PenyewaDAO dao =
        new PenyewaDAO();

        if (dao.simpan(
            penyewa)) {

        JOptionPane.showMessageDialog(
            this,
            "Data berhasil disimpan!");

        bersihForm();

        generateKode();

        loadData();

        } else {

            JOptionPane.showMessageDialog(
                this,
                "Data gagal disimpan!");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        if (idPenyewa == 0) {

            JOptionPane.showMessageDialog(
                this,
                "Pilih data terlebih dahulu!");

            return;
        }

        Penyewa penyewa =
        new Penyewa();

        penyewa.setIdPenyewa(
            idPenyewa);

        penyewa.setKodePenyewa(
            txtKodePenyewa.getText());

        penyewa.setNama(
            txtNama.getText().trim());

        penyewa.setAlamat(
            txtAlamat.getText().trim());

        penyewa.setNoHp(
            txtNoHp.getText().trim());

        PenyewaDAO dao =
        new PenyewaDAO();

        if (dao.ubah(
            penyewa)) {

        JOptionPane.showMessageDialog(
            this,
            "Data berhasil diubah!");

        idPenyewa = 0;

        bersihForm();

        generateKode();

        loadData();

        } else {

            JOptionPane.showMessageDialog(
                this,
                "Data gagal diubah!");
        }
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        if (idPenyewa == 0) {

            JOptionPane.showMessageDialog(
                this,
                "Pilih data terlebih dahulu!");

            return;
        }

        int konfirmasi =
        JOptionPane.showConfirmDialog(
            this,
            "Yakin ingin menghapus data?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION);

        if (konfirmasi
            == JOptionPane.YES_OPTION) {

            PenyewaDAO dao =
            new PenyewaDAO();

            if (dao.hapus(
                idPenyewa)) {

            JOptionPane.showMessageDialog(
                this,
                "Data berhasil dihapus!");

            idPenyewa = 0;

            bersihForm();

            generateKode();

            loadData();

        } else {

            JOptionPane.showMessageDialog(
                this,
                "Data gagal dihapus!");
        }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

