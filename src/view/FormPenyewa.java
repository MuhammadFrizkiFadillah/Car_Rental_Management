    private void tblPenyewaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPenyewaMouseClicked
        int row =
        tblPenyewa.getSelectedRow();

        if (row != -1) {

            idPenyewa =
            Integer.parseInt(
                tblPenyewa.getValueAt(
                    row,
                    0).toString());

            txtKodePenyewa.setText(
                tblPenyewa.getValueAt(
                    row,
                    1).toString());

            txtNama.setText(
                tblPenyewa.getValueAt(
                    row,
                    2).toString());

            txtAlamat.setText(
                tblPenyewa.getValueAt(
                    row,
                    3).toString());

            txtNoHp.setText(
                tblPenyewa.getValueAt(
                    row,
                    4).toString());
        }
    }//GEN-LAST:event_tblPenyewaMouseClicked

    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyReleased
        DefaultTableModel model =
        (DefaultTableModel)
        tblPenyewa.getModel();

        model.setRowCount(0);

        PenyewaDAO dao =
        new PenyewaDAO();

        List<Penyewa> list =
        dao.cari(
            txtCari.getText());

        for (Penyewa p : list) {

            Object[] row = {

                p.getIdPenyewa(),
                p.getKodePenyewa(),
                p.getNama(),
                p.getAlamat(),
                p.getNoHp()

            };

            model.addRow(row);
        }
    }//GEN-LAST:event_txtCariKeyReleased
private void bersihForm() {

    idPenyewa = 0;

    txtNama.setText("");
    txtAlamat.setText("");
    txtNoHp.setText("");
    txtCari.setText("");
}
private void generateKode() {

    String kode =
            KodeGenerator.generateKode(
                    "penyewa",
                    "kode_penyewa",
                    "PNY");

    txtKodePenyewa.setText(kode);
}
private void loadData() {

    DefaultTableModel model =
            (DefaultTableModel)
            tblPenyewa.getModel();

    model.setRowCount(0);

    PenyewaDAO dao =
            new PenyewaDAO();

    List<Penyewa> list =
            dao.getAll();

    for (Penyewa p : list) {

        Object[] row = {

            p.getIdPenyewa(),
            p.getKodePenyewa(),
            p.getNama(),
            p.getAlamat(),
            p.getNoHp()

        };

        model.addRow(row);
    }
}
private void aturKolomTabel() {

    DefaultTableModel model =
            new DefaultTableModel(
                    new Object[][] {},
                    new String[] {
                        "ID",
                        "Kode Penyewa",
                        "Nama",
                        "Alamat",
                        "No HP"
                    }) {

        @Override
        public boolean isCellEditable(
                int row,
                int column) {

            return false;
        }
    };

    tblPenyewa.setModel(model);

    tblPenyewa.getColumnModel()
            .getColumn(0)
            .setPreferredWidth(50);

    tblPenyewa.getColumnModel()
            .getColumn(1)
            .setPreferredWidth(100);

    tblPenyewa.getColumnModel()
            .getColumn(2)
            .setPreferredWidth(150);

    tblPenyewa.getColumnModel()
            .getColumn(3)
            .setPreferredWidth(250);

    tblPenyewa.getColumnModel()
            .getColumn(4)
            .setPreferredWidth(120);
}
