    private void tblKendaraanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKendaraanMouseClicked
int row =
        tblKendaraan.getSelectedRow();

if (row != -1) {

    idKendaraan =
            Integer.parseInt(
                    tblKendaraan
                            .getValueAt(
                                    row,
                                    0)
                            .toString());

    txtKodeKendaraan.setText(
            tblKendaraan
                    .getValueAt(
                            row,
                            1)
                    .toString());

    txtMerk.setText(
            tblKendaraan
                    .getValueAt(
                            row,
                            2)
                    .toString());

    txtModel.setText(
            tblKendaraan
                    .getValueAt(
                            row,
                            3)
                    .toString());

    cmbJenisKendaraan
            .setSelectedItem(
                    tblKendaraan
                            .getValueAt(
                                    row,
                                    4)
                            .toString());

    txtPlatNomor.setText(
            tblKendaraan
                    .getValueAt(
                            row,
                            5)
                    .toString());

    txtTahun.setText(
            tblKendaraan
                    .getValueAt(
                            row,
                            6)
                    .toString());

    txtWarna.setText(
            tblKendaraan
                    .getValueAt(
                            row,
                            7)
                    .toString());

    txtHargaSewa.setText(
            tblKendaraan
                    .getValueAt(
                            row,
                            8)
                    .toString());

    cmbStatus.setSelectedItem(
            tblKendaraan
                    .getValueAt(
                            row,
                            9)
                    .toString());
}
    }//GEN-LAST:event_tblKendaraanMouseClicked

    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyReleased
DefaultTableModel model =
        (DefaultTableModel)
        tblKendaraan.getModel();

model.setRowCount(0);

KendaraanDAO dao =
        new KendaraanDAO();

List<Kendaraan> list =
        dao.cari(
                txtCari.getText());

for (Kendaraan k : list) {

    model.addRow(
            new Object[] {

                k.getIdKendaraan(),
                k.getKodeKendaraan(),
                k.getMerk(),
                k.getModel(),
                k.getJenisKendaraan(),
                k.getPlatNomor(),
                k.getTahun(),
                k.getWarna(),
                k.getHargaSewa(),
                k.getStatus()

            });
}
    }//GEN-LAST:event_txtCariKeyReleased
