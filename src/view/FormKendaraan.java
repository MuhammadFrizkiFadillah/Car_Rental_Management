    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
String kode =
        txtKodeKendaraan.getText();

String merk =
        txtMerk.getText().trim();

String model =
        txtModel.getText().trim();

String plat =
        txtPlatNomor.getText().trim();

String warna =
        txtWarna.getText().trim();

String jenis =
        cmbJenisKendaraan
                .getSelectedItem()
                .toString();

String status =
        cmbStatus
                .getSelectedItem()
                .toString();

if (merk.isEmpty()
        || model.isEmpty()
        || plat.isEmpty()
        || txtTahun.getText().trim().isEmpty()
        || warna.isEmpty()
        || txtHargaSewa.getText().trim().isEmpty()) {

    JOptionPane.showMessageDialog(
            this,
            "Semua data wajib diisi!");

    return;
}

try {

    int tahun =
            Integer.parseInt(
                    txtTahun.getText()
                            .trim());

    double hargaSewa =
            Double.parseDouble(
                    txtHargaSewa.getText()
                            .trim());

    Kendaraan kendaraan;

    switch (jenis) {

        case "SUV":
            kendaraan =
                    new MobilSUV();
            break;

        case "MPV":
            kendaraan =
                    new MobilMPV();
            break;

        case "SEDAN":
            kendaraan =
                    new MobilSedan();
            break;

        default:

            JOptionPane.showMessageDialog(
                    this,
                    "Jenis kendaraan tidak valid!");

            return;
    }

    kendaraan.setKodeKendaraan(
            kode);

    kendaraan.setMerk(
            merk);

    kendaraan.setModel(
            model);

    kendaraan.setJenisKendaraan(
            jenis);

    kendaraan.setPlatNomor(
            plat);

    kendaraan.setTahun(
            tahun);

    kendaraan.setWarna(
            warna);

    kendaraan.setHargaSewa(
            hargaSewa);

    kendaraan.setStatus(
            status);

    kendaraan.setDeleted(
            false);

    KendaraanDAO dao =
            new KendaraanDAO();

    if (dao.simpan(
            kendaraan)) {

        JOptionPane.showMessageDialog(
                this,
                "Data kendaraan berhasil disimpan!");

        bersihForm();

        generateKode();

        loadData();

    } else {

        JOptionPane.showMessageDialog(
                this,
                "Data kendaraan gagal disimpan!");
    }

} catch (NumberFormatException e) {

    JOptionPane.showMessageDialog(
            this,
            "Tahun dan Harga Sewa harus berupa angka!");
}
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
if (idKendaraan == 0) {

    JOptionPane.showMessageDialog(
            this,
            "Pilih data terlebih dahulu!");

    return;
}

try {

    Kendaraan kendaraan;

    String jenis =
            cmbJenisKendaraan
                    .getSelectedItem()
                    .toString();

    switch (jenis) {

        case "SUV":
            kendaraan =
                    new MobilSUV();
            break;

        case "MPV":
            kendaraan =
                    new MobilMPV();
            break;

        case "SEDAN":
            kendaraan =
                    new MobilSedan();
            break;

        default:
            return;
    }

    kendaraan.setIdKendaraan(
            idKendaraan);

    kendaraan.setKodeKendaraan(
            txtKodeKendaraan
                    .getText());

    kendaraan.setMerk(
            txtMerk.getText()
                    .trim());

    kendaraan.setModel(
            txtModel.getText()
                    .trim());

    kendaraan.setJenisKendaraan(
            jenis);

    kendaraan.setPlatNomor(
            txtPlatNomor
                    .getText()
                    .trim());

    kendaraan.setTahun(
            Integer.parseInt(
                    txtTahun
                            .getText()
                            .trim()));

    kendaraan.setWarna(
            txtWarna.getText()
                    .trim());

    kendaraan.setHargaSewa(
            Double.parseDouble(
                    txtHargaSewa
                            .getText()
                            .trim()));

    kendaraan.setStatus(
            cmbStatus
                    .getSelectedItem()
                    .toString());

    KendaraanDAO dao =
            new KendaraanDAO();

    if (dao.ubah(
            kendaraan)) {

        JOptionPane.showMessageDialog(
                this,
                "Data berhasil diubah!");

        bersihForm();

        generateKode();

        loadData();

    } else {

        JOptionPane.showMessageDialog(
                this,
                "Data gagal diubah!");
    }

} catch (NumberFormatException e) {

    JOptionPane.showMessageDialog(
            this,
            "Tahun dan Harga harus angka!");
}
    }//GEN-LAST:event_btnUbahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
if (idKendaraan == 0) {

    JOptionPane.showMessageDialog(
            this,
            "Pilih data terlebih dahulu!");

    return;
}

int konfirmasi =
        JOptionPane.showConfirmDialog(
                this,
                "Yakin ingin menghapus data?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION);

if (konfirmasi
        == JOptionPane.YES_OPTION) {

    KendaraanDAO dao =
            new KendaraanDAO();

    if (dao.hapus(
            idKendaraan)) {

        JOptionPane.showMessageDialog(
                this,
                "Data berhasil dihapus!");

        bersihForm();

        generateKode();

        loadData();
    }
}
    }//GEN-LAST:event_btnHapusActionPerformed

