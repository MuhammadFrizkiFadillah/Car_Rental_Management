    private void tblPengembalianMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPengembalianMouseClicked
    int row =
            tblPengembalian.getSelectedRow();

    if (row != -1) {

        txtKodePengembalian.setText(
                tblPengembalian
                        .getValueAt(row, 1)
                        .toString());

        txtHariTerlambat.setText(
                tblPengembalian
                        .getValueAt(row, 5)
                        .toString());

        txtDenda.setText(
                tblPengembalian
                        .getValueAt(row, 6)
                        .toString());
    }
    }//GEN-LAST:event_tblPengembalianMouseClicked

    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyReleased
    DefaultTableModel model =
            (DefaultTableModel)
            tblPengembalian.getModel();

    model.setRowCount(0);

    PenyewaanDAO dao =
            new PenyewaanDAO();

    List<Penyewaan> list =
            dao.cari(
                    txtCari.getText());

    for (Penyewaan s : list) {

        model.addRow(
                new Object[] {

                    s.getIdSewa(),
                    s.getKodeSewa(),
                    s.getPenyewa()
                            .getNama(),

                    s.getKendaraan()
                            .getMerk()
                            + " "
                            + s.getKendaraan()
                                    .getModel(),

                    s.getTanggalSewa(),

                    s.getLamaSewa(),

                    s.getTanggalJatuhTempo(),

                    s.getHargaSewaHarian(),

                    s.getTotalBayar()

                });
    }
    }//GEN-LAST:event_txtCariKeyReleased

