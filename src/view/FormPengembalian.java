    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
    try {

        Penyewaan penyewaan =
                (Penyewaan)
                cmbPenyewaan.getSelectedItem();

        if (penyewaan == null) {

            JOptionPane.showMessageDialog(
                    this,
                    "Pilih data penyewaan!");

            return;
        }

        Date tanggal =
                dcTanggalKembali.getDate();

        if (tanggal == null) {

            JOptionPane.showMessageDialog(
                    this,
                    "Tanggal kembali wajib diisi!");

            return;
        }

        LocalDate tanggalKembali =
                tanggal.toInstant()
                        .atZone(
                                ZoneId.systemDefault())
                        .toLocalDate();

        Pengembalian pengembalian =
                new Pengembalian();

        pengembalian.setKodePengembalian(
                txtKodePengembalian.getText());

        pengembalian.setPenyewaan(
                penyewaan);

        pengembalian.setTanggalKembali(
                tanggalKembali);

        pengembalian.setHariTerlambat(
                Integer.parseInt(
                        txtHariTerlambat.getText()));

        pengembalian.setTotalDenda(
                Double.parseDouble(
                        txtDenda.getText()));

        PengembalianDAO dao =
                new PengembalianDAO();

        if (dao.simpan(
                pengembalian)) {

            JOptionPane.showMessageDialog(
                    this,
                    "Pengembalian berhasil disimpan!");

            bersihForm();

            generateKode();

            loadComboPenyewaan();

            loadData();

        } else {

            JOptionPane.showMessageDialog(
                    this,
                    "Pengembalian gagal disimpan!");
        }

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());
    }
    }//GEN-LAST:event_btnSimpanActionPerformed

