    private void txtCariKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyReleased
    DefaultTableModel model =
            (DefaultTableModel)
            tblPenyewaan.getModel();

    model.setRowCount(0);

    PenyewaanDAO dao =
            new PenyewaanDAO();

    List<Penyewaan> list =
            dao.cari(
                    txtCari.getText());

    for (Penyewaan s : list) {

        model.addRow(
                new Object[] {

                    s.getIdSewa(),
                    s.getKodeSewa(),
                    s.getPenyewa()
                            .getNama(),

                    s.getKendaraan()
                            .getMerk()
                            + " "
                            + s.getKendaraan()
                                    .getModel(),

                    s.getTanggalSewa(),

                    s.getLamaSewa(),

                    s.getTanggalJatuhTempo(),

                    s.getHargaSewaHarian(),

                    s.getTotalBayar()

                });
    }
    }//GEN-LAST:event_txtCariKeyReleased

