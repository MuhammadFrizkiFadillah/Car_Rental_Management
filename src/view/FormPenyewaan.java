private void hitungTotalBayar() {

    try {

        double harga =
                Double.parseDouble(
                        txtHargaSewa
                                .getText());

        int lama =
                Integer.parseInt(
                        txtLamaSewa
                                .getText());

        double total =
                harga * lama;

        txtTotalBayar.setText(
                String.valueOf(
                        total));

    } catch (Exception e) {

        txtTotalBayar.setText(
                "0");
    }
}
private void hitungJatuhTempo() {

    try {

        Date tanggal =
                dcTanggalSewa.getDate();

        if (tanggal == null) {
            return;
        }

        int lama =
                Integer.parseInt(
                        txtLamaSewa
                                .getText());

        LocalDate localDate =
                tanggal.toInstant()
                        .atZone(
                                ZoneId.systemDefault())
                        .toLocalDate();

        LocalDate jatuhTempo =
                localDate.plusDays(
                        lama);

        txtTanggalJatuhTempo
                .setText(
                        jatuhTempo
                                .toString());

    } catch (Exception e) {

        txtTanggalJatuhTempo
                .setText("");
    }
}
