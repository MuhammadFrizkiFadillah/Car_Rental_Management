    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
try {

    Penyewa penyewa =
            (Penyewa)
            cmbPenyewa.getSelectedItem();

    Kendaraan kendaraan =
            (Kendaraan)
            cmbKendaraan.getSelectedItem();

    if (penyewa == null
            || kendaraan == null) {

        JOptionPane.showMessageDialog(
                this,
                "Penyewa atau kendaraan belum dipilih!");

        return;
    }

    Date tanggal =
            dcTanggalSewa.getDate();

    if (tanggal == null) {

        JOptionPane.showMessageDialog(
                this,
                "Tanggal sewa wajib diisi!");

        return;
    }

    LocalDate tanggalSewa =
            tanggal.toInstant()
                    .atZone(
                            ZoneId.systemDefault())
                    .toLocalDate();

    int lamaSewa =
            Integer.parseInt(
                    txtLamaSewa.getText()
                            .trim());

    LocalDate jatuhTempo =
            tanggalSewa.plusDays(
                    lamaSewa);

    Penyewaan sewa =
            new Penyewaan();

    sewa.setKodeSewa(
            txtKodeSewa.getText());

    sewa.setPenyewa(
            penyewa);

    sewa.setKendaraan(
            kendaraan);

    sewa.setTanggalSewa(
            tanggalSewa);

    sewa.setLamaSewa(
            lamaSewa);

    sewa.setTanggalJatuhTempo(
            jatuhTempo);

    sewa.setHargaSewaHarian(
            kendaraan.getHargaSewa());

    sewa.setTotalBayar(
            Double.parseDouble(
                    txtTotalBayar.getText()));

    PenyewaanDAO dao =
            new PenyewaanDAO();

    if (dao.simpan(sewa)) {

        JOptionPane.showMessageDialog(
                this,
                "Penyewaan berhasil disimpan!");

        bersihForm();

        generateKode();

        loadComboKendaraan();

        loadData();

    } else {

        JOptionPane.showMessageDialog(
                this,
                "Penyewaan gagal disimpan!");
    }

} catch (NumberFormatException e) {

    JOptionPane.showMessageDialog(
            this,
            "Lama sewa harus berupa angka!");
}
    }//GEN-LAST:event_btnSimpanActionPerformed

